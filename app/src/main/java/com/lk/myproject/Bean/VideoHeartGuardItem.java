package com.lk.myproject.Bean;


public class VideoHeartGuardItem {
    public int age;
    public int anchorId;
    public String avatar;
    public String city;
    public String label;
    public String nickName;
    public int score;
    public int tuhaoId;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAnchorId() {
        return anchorId;
    }

    public void setAnchorId(int anchorId) {
        this.anchorId = anchorId;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getTuhaoId() {
        return tuhaoId;
    }

    public void setTuhaoId(int tuhaoId) {
        this.tuhaoId = tuhaoId;
    }
}
